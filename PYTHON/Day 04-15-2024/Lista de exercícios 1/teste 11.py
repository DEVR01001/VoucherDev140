#### Soma de número inteiros do sucessor de seu tiplo com o antecessor de seu dobro ####
numero1 = int(input("Digite um número inteiro: "))
sucessor = (numero1+1)*3
antecessor = (numero1-1)*2
soma= sucessor + antecessor
print(f"A soma do sucessor({sucessor}) e antecessor ({antecessor}) do seu número é ({soma})") 
