###### Calculo da média de notas ########
print("Bem vindo ao calculo da média de notas!!!")
medianum1= float(input("\nDigite a primeira nota: "))
medianum2= float(input("\nDigite a segunda nota: "))
medianum3= float(input("\nDigite a terceira nota: "))
medianum4 = float(input("\nDigite a quarta nota: "))
mediamult = (medianum1+medianum2+ medianum3+ medianum4)/4
print(f"\nA média das notas são {mediamult}")
