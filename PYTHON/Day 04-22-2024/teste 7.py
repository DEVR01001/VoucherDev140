#### Faça um Programa que verifique se uma letra digitada é "F" ou "M". Conforme a letra escreva: F ####
#### - Feminino, M – Masculino ou Sexo Inválido ###

sexo= input("Digite (M) se for do sexo Masculino senão digite (F) se for do seo feminino: ")
if(sexo == "M"):
    print("Masculino")
elif(sexo== "F"):
    print("Feminino")
else: 
    print("Sexo Inválido")

