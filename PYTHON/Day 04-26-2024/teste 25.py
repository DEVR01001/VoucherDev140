#### Escreva um programa que leia um inteiro entre 1 e 7 e imprima o dia da semana correspondente #### 
#### a este número. Isto é, domingo equivale a 1, segunda-feira se 2, e assim por diante. ####

diasemana= int(input("Digite um núemro de 1 á 7: "))

if (diasemana== 1):
    print(" Hoje é Domingo")
elif (diasemana== 2):
    print(" Hoje é Segunda")
elif (diasemana== 3):
    print(" Hoje é Terça")
elif (diasemana== 4):
    print(" Hoje é Quarta")
elif (diasemana== 5):
    print(" Hoje é Quinta")
elif (diasemana== 6):
    print(" Hoje é Sexta")
else:
    print(" Hoje é Sábado")
